class Asset {
  static getImg(String imgName) {
    return "assets/images/$imgName";
  }

  static getIcon(String iconName) {
    return "assets/icons/$iconName";
  }

  static getSvg(String svgName) {
    return "assets/svgs/$svgName";
  }

  static getGif(String gifName) {
    return "assets/gifs/$gifName";
  }
}
